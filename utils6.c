#include "ft_printf.h"

char	*ft_insert_minus(char *str)
{
	char	*dest;
	size_t	i;

	i = 0;
	dest = ft_strdup(str);
	while (str[i] == ' ')
		i++;
	dest[i - 1] = '-';
	return (dest);
}
